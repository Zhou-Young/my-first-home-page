$(function(){
    var play=0;

    $("#music-play-1").click(function(){
        $("#music-img-1").addClass("rotate");

        if(play==0){
            $(this).attr("src","img/pause.png");
            $("#music-img-1").removeClass("pause");
            $("#music1").get(0).play();
            play=1;
        }else{
            $(this).attr("src","img/play.png");
            $("#music-img-1").addClass("pause");

            play=0;
            $("#music1").get(0).pause();
        }

    });

});